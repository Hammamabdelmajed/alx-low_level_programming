#include <stdlib.h>
#include <stdio.h>
#include <time.h>
/**
 * main - Entry point of the program
 *
 * Description: This program generates a random number and prints whether
 * the number is positive, zero, or negative.
 *
 * Return: Always 0 (Success)
 */
int main(void)
{
char letter;
char LETTER;
for (letter = 'a'; letter <= 'z'; letter++)
putchar(letter);
for (LETTER = 'A'; LETTER <= 'Z'; LETTER++)
putchar(LETTER);
putchar('\n');
return (0);
}
